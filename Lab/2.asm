 AREA PROG_2_4, CODE, READONLY				        ;ADDITION OF TWO 64 BIT NUMBER
	ENTRY
	LDR R0,=0xE0090000
	LDR R9,=0XE009001B

	LDR R1,[R0]
	LDR R2,[R0,#04]

	ADDS R3,R1,R2	;LOWER NIBBLE

	LDR R4,[R0,#08]
	LDR R5,[R0,#12]

	ADCS R7,R4,R5

	MOV R8,0X00
	ADC R8,R8,R8

	STR R3,[R9]  
	STR R7,[R9,#4]
	STR R8,[R9,#8]
	END